def get_palette_names() -> list[str]:
    """Return sorted list of available palette names."""
    return sorted(_PALETTES.keys())


def get_palette(name: str) -> list[Color]:
    """
    Return palette by name.

    Args:
        name: Palette name (e.g., "Endesga 32").

    Returns:
        List of (R, G, B, A) 4-tuples (copy — mutation does not affect the bundle).

    Raises:
        KeyError: If name is not in the palette bundle.
    """
    if name not in _PALETTES:
        available = ", ".join(sorted(_PALETTES.keys()))
        raise KeyError(
            f"Palette '{name}' inconnue. Palettes disponibles : {available}"
        )
    return list(_PALETTES[name])  # Return a copy to prevent mutation
